Canine Freestyle
	Dance with your dog! Combine your love for music and rhythm with obedience training in Canine Freestyle. Also know as Musical Freestyle, this sport allows for creativity to flourish as handler and dog team up together to dance their way to the top.

Rally Free
	Together the sport of Rally Obedience combined with the dance moves of Canine freestyle come together to create this upbeat fun activity. This challenging sport will put your dogs obedience and rhythm to the test!

