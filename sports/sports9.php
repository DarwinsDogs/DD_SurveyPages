Dock Diving
	Have a dog with a love for water and loves to retrieve toys? Dock diving may be the perfect sport for you! By tossing one of your dogs favorite toys into a long pool, work on their ability to jump the farthest they possibly can. This fun sport is for all size dogs!

Dog Surfing
	Chasing the sun? Live somewhere where the waves are great? Try a challenging sport for you and your dog. Take on the waves together surfing!

Swimming
	Enjoy the water? Have your dog become an expert swimmer and cool off during those hot summer days. A great skill to have for life. And even use this to try out new water sports!
