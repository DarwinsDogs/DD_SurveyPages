<div class="subsport"><span>Nose Work</span>
<p>Designed for all breeds of dogs, this sport tests their innate skill as a tracking animal. Have your dog practice identifying specific scents in a group of other distracting scents. Play this as a fun game at home, or introduce your dog into competition and challenge others!</p>
