Barn Hunt
	It’s known that dogs have a great sense of smell. Now there’s a sport for all types of dogs to get involved in and really utilize their gift. Using their powerful noses, the dogs are familiarized with the scent of the rat and then track it down in a barn! 

Earth Dog
	Have a terrier or small dog that loves to chase or track rodents? This fun safe sport harnesses that passion and puts it to the test. Have your dog navigate through a maze to track down the rat!

Lure Coursing
	Have a dog who loves to chase? Lure coursing is the perfect sport! Your dog will compete against others chasing a mechanically operated lure. Originally designed for sighthound breeds, but now have ability tests for all breeds who want to participate!

Nose Work
	Designed for all breeds of dogs, this sport tests their innate skill as a tracking animal. Have your dog practice identifying specific scents in a group of other distracting scents. Play this as a fun game at home, or introduce your dog into competition and challenge others!

Tracking
	Put those nose work skills to the test and see how far you can go. Practice your dogs distance and accuracy work when tracking different scents is new environments.

