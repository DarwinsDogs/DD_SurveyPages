Carting
	A sport for all size dogs big or small. Have your dog practice pulling light loads to start and then upgrade them to carry all the firewood! Great for anyone looking for a non-complex challenge in any kind of environment. 

Mushing
	Live somewhere cold and a dog looking to burn of steam? This dog sport incorporates teamwork and precise training for a team to work together and conquer to course. 

Weight Pulling
	This fun activity, when done properly, is perfect for all dog sizes. Have your dog work on their strength training and pulling skills getting comfortable having something behind them.
