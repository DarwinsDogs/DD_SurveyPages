<div class="subsport"><span>Bikejoring</span>
<p>Enjoy biking? Looking for a new way to get out there with your dog? Also known as Urban mushing, Bikejoring is a fun easy activity that will allow for you and your dog to exercise outdoors together. By teaching your dog a few simple directional commands and with some training, they’ll learn to run in front while attached to bungie cord, and pull you through any trail.</p>
