Herding
	Originally designed for the herding breeds, but truly all breeds of dogs are welcome to the sport. Have your dog practice on keeping different species of animals together in a tight group.

Treibball
	A fun herding game for you and your dog. Have them practice their soccer skills by herding exercise balls into a goal. Use many different size balls for variety and challenge.
